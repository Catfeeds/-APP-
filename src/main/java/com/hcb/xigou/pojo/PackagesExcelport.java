public class PackagesExcelport {
	private String title;
	
	private String couponNumbers;
	
	private String packeageStock;
	
	private String packageStatus;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getCouponNumbers() {
		return couponNumbers;
	}

	public void setCouponNumbers(String couponNumbers) {
		this.couponNumbers = couponNumbers;
	}

	public String getPackeageStock() {
		return packeageStock;
	}

	public void setPackeageStock(String packeageStock) {
		this.packeageStock = packeageStock;
	}

	public String getPackageStatus() {
		return packageStatus;
	}

	public void setPackageStatus(String packageStatus) {
		this.packageStatus = packageStatus;
	}
	
}
